'''
Escreva um programa que permite ao usuário digitar dois números inteiros e exibir o resultado para cada uma das seguintes operações:

(+, -, *, /, //, %, **)

'''

n1 = int(input())
n2 = int(input())

print(n1 + n2) # soma
print(n1 - n2) # subtração
print(n1 * n2) # multiplicação
print(n1 / n2) # divisão
print(n1 // n2) # divisão inteira
print(n1 % n2) # resto da divisão
print(n1 ** n2) # potência