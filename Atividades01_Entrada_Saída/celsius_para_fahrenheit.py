'''
Um termômetro exibe a temperatura em graus Centígrados. Faça um programa para efetuar essa conversão e ao final
exibir essa temperaturaem graus Fahrenheit.
'''

print('Convertor de Celsius para Fahrenheit')
C = int(input('Valor em Celsius: '))
fahrenheit = 1.8 * C + 32
print ('O valor em Fahrenheit é', fahrenheit)